package yeniden;

public class C12_IliskiselOperatorler {


    /* genelde bunlari karsilastirma durumlarinda kullaniriz


        > buyuk
        < kucuk
        >=  buyuk yada esit
        <= lucuk yada esit
        == esit mi
        != esit degil mi
     */

    public static void main(String[] args) {

      int sayi1=100;
      int sayi2=90;

      //sayi1 > sayi2  == > boolean (true yada false)

        System.out.println(sayi1 > sayi2);  //true
        System.out.println(sayi1 < sayi2);  //false
        System.out.println(sayi1 >= sayi2); //true
        System.out.println(sayi1 <= sayi2); //false
        System.out.println(sayi1 == sayi2);  //false
        System.out.println(sayi1 != sayi2);  //true









    }




}
