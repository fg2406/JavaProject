package yeniden;

 public class C15_MantiksalOperator {

     public static void main(String[] args) {

        //mantiksal operatorler

         //  &&(ve)  ikisininde kosulunda true olmasi gerekir,o zaman treu yoksa false
         // || (veya)  ikisinden biri true ise true yoksa false


         int sayi1=75;
         int sayi2=20;
         int sayi3=40;
         System.out.println(sayi1>sayi2 || sayi2>sayi1);  //true
         System.out.println(sayi1>sayi2 && sayi2>sayi3);  //false,iki kosulda saglanmak zorunda








     }



}









